package com.young.SpringWorld.domain;

//고객 id, name 받음 & getter setter로 값 초기화 및 사용
public class Member {
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
